export const SP_KEYS = Object.keys(SP);

export function drawSprite(cv: HTMLCanvasElement) {
  const map = SP[cv.dataset.sp as string];
  if (!map) return;
  const tie = cv.dataset.tie;
  const sc  = parseInt(cv.dataset.sc || '4') || 4;
  const W = 10, H = 18;
  cv.width  = W * sc;
  cv.height = H * sc;
  const ctx = cv.getContext('2d');
  if (!ctx) return;
  ctx.imageSmoothingEnabled = false;
  for (let y = 0; y < H; y++) {
    for (let x = 0; x < W; x++) {
      const c = map[y]?.[x];
      if (!c) continue;
      ctx.fillStyle = (c === 5) ? (tie || '#888') : PAL[c];
      if (!ctx.fillStyle) continue;
      ctx.fillRect(x * sc, y * sc, sc, sc);
    }
  }
}
